#pragma once

// Unsigned int types. 
#define u8 uint8_t 
#define u16 uint16_t
#define u32 uint32_t
#define u64 uint64_t

// Signed int types.
#define i8 int8_t   
#define i16 int16_t 
#define i32 int32_t 
#define i64 int64_t 

// Floating point types
#define f32 float 
#define f64 double

// boolean
#define b8 bool

#define Gigabytes(count) (u64) (count * 1024 * 1024 * 1024)
#define Megabytes(count) (u64) (count * 1024 * 1024)
#define Kilobytes(count) (u64) (count * 1024)

